package introduction;

public class ProceduralExample {
  public static void main(String[] args){
	String[] names={"Jillian","Joseph","Jordan"};
    String[] years={"Freshmen","Junior","Senior"};
    System.out.println("Hi, my name is"+names[0]+"and I am in"+years[0]+".");
    System.out.println("Hi, my name is"+names[1]+"and I am in"+years[1]+".");
    System.out.println("Hi, my name is"+names[2]+"and I am in"+years[2]+".");
    System.out.println("Hello World");
    System.out.println("Bye World");
  }
}
